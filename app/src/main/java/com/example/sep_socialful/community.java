package com.example.sep_socialful;

public class community {
    String name;
    String color;

    public community() {
        this.name = name;
        this.color = color;
    }

    public community(String name, String color) {
        this.name = name;
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }
}
